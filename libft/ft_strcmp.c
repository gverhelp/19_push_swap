#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t	a;

	a = 0;
	while (s1[a] != '\0' && s2[a] != '\0' &&
			(unsigned char)s1[a] == (unsigned char)s2[a])
		a++;
	return ((unsigned char)s1[a] - (unsigned char)s2[a]);
}